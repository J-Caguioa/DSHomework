# Part 1a. Take log of a positive number.
a_pos_num <- 42
log(a_pos_num)

# Part 1b. Take log of same number with different base.
log(a_pos_num, base = exp(1))
log(a_pos_num, base = 10)

# Part 1c. Take log of a negative number.
log(-a_pos_num)

# Part 1d. Take square root of a positive number.
sqrt(a_pos_num)